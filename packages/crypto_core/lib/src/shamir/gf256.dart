/// Adds two elements in GF(256): a ⊕ b (XOR).
int gf256Add(int a, int b) => a ^ b;

/// Multiplies two elements in GF(256) using lookup tables.
int gf256Mul(int a, int b) {
  if (a == 0 || b == 0) return 0;
  return expTable[(logTable[a] + logTable[b]) % 255];
}

/// Computes the multiplicative inverse of [a] in GF(256).
///
/// Throws [ArgumentError] if [a] is 0 (no inverse).
int gf256Inv(int a) {
  if (a == 0) throw ArgumentError('No inverse for 0 in GF(256)');
  return expTable[255 - logTable[a]];
}

/// Divides [a] by [b] in GF(256): a * b^-1.
int gf256Div(int a, int b) {
  if (b == 0) throw ArgumentError('Division by zero in GF(256)');
  if (a == 0) return 0;
  return expTable[(logTable[a] + 255 - logTable[b]) % 255];
}
